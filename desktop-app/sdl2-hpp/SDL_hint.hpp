#pragma once
#include <SDL.h>
#include <string>

namespace SDL {
	inline bool SetHint(const char *name, const char *value) {
		return SDL_SetHint(name,value) == SDL_TRUE;
	}
	inline bool SetHint(const char *name, const std::string &value) {
		return SetHint(name,value.c_str()) == SDL_TRUE;
	}
	namespace Hint {
		constexpr const auto RENDER_SCALE_QUALITY = SDL_HINT_RENDER_SCALE_QUALITY;
		enum RenderScaleQuality: char {
			RENDER_SCALE_QUALITY_NEAREST = '0',
			RENDER_SCALE_QUALITY_LINEAR  = '1',
			RENDER_SCALE_QUALITY_BEST    = '2',
		};
		inline bool SetRenderScaleQuality(RenderScaleQuality value) {
			char hint_value[2] = {static_cast<char>(value),'\0'};
			return SetHint(RENDER_SCALE_QUALITY,hint_value);
		}
	}
}
